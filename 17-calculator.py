a=int(input("enter number 1:"))
b=int(input("enter number 2:"))
c=input("enter '+' for Addition , '-' for Subraction , '* ' for Multiplication , '/' for Division'): " )

if c=="+":
    print("Answer:",a+b)
elif c=="-":
    print("Answer  :",a - b)
elif c=="*":
    print("Answer",a*b)
elif c=="/":
    print("Answer",a/b)
